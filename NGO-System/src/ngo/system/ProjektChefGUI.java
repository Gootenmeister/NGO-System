    private void projektBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_projektBoxActionPerformed
        //hämtar kostnad för projektet och placerar det nummret i en jLabel
        String fetchKostnad = "select kostnad from projekt where projektnamn = ";
        String valtProjekt = (String) projektBox.getSelectedItem();
        try {
            String utskriftKostnad = idb.fetchSingle(fetchKostnad + "'" + valtProjekt + "'");
            kostnadLabel.setText(utskriftKostnad + " kr");
        }
        catch(InfException ex) {
            System.out.println("Error: " +ex);
        }
        
        //hämtar land ID för att identifiera landet
        String fetchLandID = "select land from projekt where projektnamn = ";
        try {
            //hämtar land info och placerar informationen i ett textfält
            //
            String lid = idb.fetchSingle(fetchLandID + "'" + valtProjekt + "'");
            //hämtar landnamn, landspråk, osv, för landet och placerar i textfält. alla gör samma sak bara för olika kolumner
            String fetchLand = "select namn from land where lid = ";
            String landNamn = idb.fetchSingle(fetchLand + lid);
            
            String fetchLandSprak = "select sprak from land where lid = ";
            String landSprak = idb.fetchSingle(fetchLandSprak + lid);
            
            String fetchLandValuta = "select valuta from land where lid = ";
            String landValuta = idb.fetchSingle(fetchLandValuta + lid);
            
            String fetchLandTidszon = "select tidszon from land where lid = ";
            String landTidszon = idb.fetchSingle(fetchLandTidszon + lid);
            
            String fetchLandPolitik = "select politisk_struktur from land where lid = ";
            String landPolitik = idb.fetchSingle(fetchLandPolitik + lid);
            
            String fetchLandEkomoni = "select ekonomi from land where lid = ";
            String landEkomoni = idb.fetchSingle(fetchLandEkomoni + lid);
            
            //sätter in informationen i textfältet
            String landInfo = (landNamn + ", " + landSprak + ", " + landValuta + ", " + landTidszon + ", " + landPolitik + ", " + landEkomoni);
            tfLandInfo.setText(landInfo);
            
            
            //HÄMTA PARTNER INFO:
            //
            //
            //
            //hämtar projekt ID för att identifiera projektet
            String ProjektID = "select pid from projekt where projektnamn = '" + valtProjekt + "'";
            String pid = idb.fetchSingle(ProjektID);
            
            //hämtar partner namn
            String fetchPartnerNamn = "select namn from partner where pid = " + pid;
            String partnerNamn = idb.fetchSingle(fetchPartnerNamn);
            //hämtar partner branch
            String fetchPartnerBranch = "select branch from partner where pid = " + pid;
            String partnerBranch = idb.fetchSingle(fetchPartnerBranch);
            
            //hämtar först stad ID, för att sedan hämta stad namn iform tabellen "stad"
            String fetchPartnerStadID = "select stad from partner where pid = " + pid;
            String partnerStadID = idb.fetchSingle(fetchPartnerStadID);
            String fetchPartnerStadNamn = "select namn from stad where sid = " + partnerStadID;
            String partnerStadNamn = idb.fetchSingle(fetchPartnerStadNamn);
            
            //hämtar först land ID, för att sedan hämta land namn iform tabellen "land". 
            //denna är separat från "landNamn", om projektet inte urförs i samma land som partnern 'r lokaliserad
            String fetchPartnerLandID = "select land from stad where sid = " + partnerStadID;
            String partnerLandID = idb.fetchSingle(fetchPartnerLandID);
            String fetchPartnerLandNamn = "select namn from land where lid = " + partnerLandID;
            String partnerLandNamn = idb.fetchSingle(fetchPartnerLandNamn);
            
            //sätter in informationen i textfältet
            String partnerInfo = (partnerNamn + ", " + partnerBranch + ", " + partnerLandNamn + ", " + partnerStadNamn);
            tfPartnerInfo.setText(partnerInfo);
            
            
            
        } catch (InfException ex) {
            System.getLogger(ProjektChefGUI.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
        
        
        
        
        
    }//GEN-LAST:event_projektBoxActionPerformed

